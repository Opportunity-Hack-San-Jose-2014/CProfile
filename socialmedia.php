	function getFacebookByPage(paramDiv, paramPageId) {
		var reqData = 'p='+paramPageId;
		var fbPostHtml = '';
		//alert(reqData);
		$.ajax({
			  url: "http://www.cprofile.org/uss/getFacebookFeedByPage.php",
			  data: reqData
			}).done(function (respData) {
				eval('respData='+respData, 'javascript');
				for (i=0; i<respData['data'].length; i++) {
					//alert(fbPosts['data'][i]['message']);
					var profId = respData['data'][i]['from']['id'];
					fbPostHtml = fbPostHtml + '<tr>';
					fbPostHtml = fbPostHtml + '<td>'+respData['data'][i]['created_time']+'</td>';
					fbPostHtml = fbPostHtml + '<td><img src=https://graph.facebook.com/'+ profId +'/picture></td>';
					fbPostHtml = fbPostHtml + '<td><a href=fbuser.php?id='+ profId +'>'+respData['data'][i]['from']['name']+'</a></td>';
					fbPostHtml = fbPostHtml + '<td>'+respData['data'][i]['message']+'</td>';
					fbPostHtml = fbPostHtml + '</tr>';
				}
				fbPostHtml = '<table class="table">'
	                +'<thead>'
	                +'<tr>'
	                +'<th>Date & Time</th>'
	                +'<th>Profile Pic</th>'
	                +'<th>Name</th>'
	                +'<th>Message</th>'
	                +'</tr>'
	                +'</thead>'
	                +'<tbody>'
	                +fbPostHtml
	                +'</tbody>'
	            	+'</table>';
				//alert(fbPostHtml);
				document.getElementById(paramDiv).innerHTML = fbPostHtml;
			});
	}

